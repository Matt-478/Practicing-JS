// Bitwise Operations
function bitwiseAND(n1, n2) {
	let result = n1 & n2
  return result
}

function bitwiseOR(n1, n2) {
	let result = n1 | n2
  return result
}

function bitwiseXOR(n1, n2) {
	let result = n1 ^ n2
	return result
}